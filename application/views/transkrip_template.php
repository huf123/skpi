					The Indonesian National Qualification Framework (IQF) is a framework denoting levels of Indonesian workforce qualifications and competence. The framework compares, equalizes, and integrates the education and training sectors as well as work experience in a scheme recognizing work competence based on the structures of various work sectors. The framework manifests the quality and identity of the Indonesians in terms of national education system, national workforce training system, and standardized national learning outcomes. It works as a reference to produce qualified and productive human resources. IQF is an independent system bridging the education and training sectors to establish national human resources. It is qualified and certified through the sceheme of formal, non-formal and informal education, job training or work experience. The qualification level is the level of nationally standardized learning outcomes based on the outcome of education process and/or training acquired through formal education, non-formal, informal, or work experience as shown in Figure 1. IQF consists of nine (9) levels of qualification, starting from the scale of 1 to 9 from the lowest to the highest. IQF has general and specific descriptors. The general descriptors indicate the character, the personality, the work attitude, ethics and moral applicable at every level of the framework while specific descriptors demonstrate the scope of science, knowledge, understanding (know-how) and skills of student's capabilities referring to IQF qualification.      
