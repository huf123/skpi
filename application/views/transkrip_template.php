				<p>
					Kerangka Kualifikasi Nasional Indonesia (KKNI) adalah kerangka penjenjangan kualifikasi dan kompetensei tenaga kerja Indonesia yang menyandingkan, menyetarakan, dan mengintegrasikan sektor pendidikan dengan sektor pelatihan dan pengalaman kerja dalam suatu skema pengakuan kemampuan kerja yang disesuaikan dengan struktur di berbagai sektor pekerjaan. KKNI merupakan perwujudan mutu dan jati diri Bangsa Indonesia terkait dengan sistem pendidikan nasional, sistem pelatihan kerja nasional serta penilaian kesetaraan capaian pembelajaran nasional, yang dimiliki Indonesia sebagai acuan dalam menghasilkan sumberdaya manusia yang bermutu dan produktif. Jenjang kualifikasi dalam KKNI adalah tingkat capaian pembelajaran yang disepakati secara nasional, disusun berdasarkan ukuran hasil pendidikan dan/atau pelatihan yang diperoleh melalui pendidikan formal, nonformal, informal, atau pengalaman kerja. Jenjang kualifikasi, dimulai dari kualifikasi 1 sebagai kualifikasi terendah hingga kualifikasi 9 sebagai kualifikasi tertinggi. Setiap sektor dan jenjang kualifikasi memiliki deskriptor tersendiri yang terdiri dari deskripsi umum dan deskripsi spesifik. Deskripsi umum mendeskripsikan karakter, kepribadian, sikap dalam berkarya, etika, moral dari setiap manusia dan berlaku pada setiap jenjang. Deskripsi spesifik mendiskripsikan cakupan keilmuan (science), pengetahuan (knowledge), pemahaman dan keterampilan yang telah dikuasai seseorang bergantung pada jenjangnya. 
